class Book {
  String image;
  String title;
  String publisher;
  String author;
  String description;
  String pagesCount;
  String wordsCount;
  String language;
  String level;
  Book({
    required this.image,
    required this.title,
    required this.publisher,
    required this.author,
    required this.description,
    required this.pagesCount,
    required this.wordsCount,
    required this.language,
    required this.level,
  });
}
