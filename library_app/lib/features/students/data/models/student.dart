import '../../domain/entities/student.dart';

class StudentModel extends Student {
  const StudentModel({
    required super.studentId,
    required super.englishName,
    required super.arabicName,
    required super.academicYear,
    required super.dateOfAdmission,
    required super.dateOfBirth,
    required super.profilePicture,
    required super.rollNo,
    required super.isActive,
    required super.address1,
    required super.address2,
    required super.city,
    required super.state,
    required super.zipcode,
    required super.country,
    required super.parentEmail,
    required super.parentAddress1,
    required super.parentAddress2,
    required super.parentCity,
    required super.parentState,
    required super.parentZipcode,
    required super.parentCountry,
    required super.sectionId,
    required super.sectionEnglishName,
    required super.sectionArabicName,
    required super.classYearId,
    required super.classYearEnglishName,
    required super.classYearArabicName,
    required super.schoolId,
    required super.schoolEnglishName,
    required super.schoolArabicName,
  });

  factory StudentModel.fromJson(Map<String, dynamic> json) => StudentModel(
        studentId: json["studentID"],
        englishName: json["englishName"],
        arabicName: json["arabicName"],
        academicYear: json["academicYear"],
        dateOfAdmission: json["dateOfAdmission"],
        dateOfBirth: DateTime.parse(json["dateOfBirth"]),
        profilePicture: json["profilePicture"],
        rollNo: json["rollNo"],
        isActive: json["isActive"],
        address1: json["address1"],
        address2: json["address2"],
        city: json["city"],
        state: json["state"],
        zipcode: json["zipcode"],
        country: json["country"],
        parentEmail: json["parentEmail"],
        parentAddress1: json["parentAddress1"],
        parentAddress2: json["parentAddress2"],
        parentCity: json["parentCity"],
        parentState: json["parentState"],
        parentZipcode: json["parentZipcode"],
        parentCountry: json["parentCountry"],
        sectionId: json["sectionID"],
        sectionEnglishName: json["sectionEnglishName"],
        sectionArabicName: json["sectionArabicName"],
        classYearId: json["classYearID"],
        classYearEnglishName: json["classYearEnglishName"],
        classYearArabicName: json["classYearArabicName"],
        schoolId: json["schoolID"],
        schoolEnglishName: json["schoolEnglishName"],
        schoolArabicName: json["schoolArabicName"],
      );

  Map<String, dynamic> toJson() => {
        "studentID": studentId,
        "englishName": englishName,
        "arabicName": arabicName,
        "academicYear": academicYear,
        "dateOfAdmission": dateOfAdmission,
        "dateOfBirth": dateOfBirth.toIso8601String(),
        "profilePicture": profilePicture,
        "rollNo": rollNo,
        "isActive": isActive,
        "address1": address1,
        "address2": address2,
        "city": city,
        "state": state,
        "zipcode": zipcode,
        "country": country,
        "parentEmail": parentEmail,
        "parentAddress1": parentAddress1,
        "parentAddress2": parentAddress2,
        "parentCity": parentCity,
        "parentState": parentState,
        "parentZipcode": parentZipcode,
        "parentCountry": parentCountry,
        "sectionID": sectionId,
        "sectionEnglishName": sectionEnglishName,
        "sectionArabicName": sectionArabicName,
        "classYearID": classYearId,
        "classYearEnglishName": classYearEnglishName,
        "classYearArabicName": classYearArabicName,
        "schoolID": schoolId,
        "schoolEnglishName": schoolEnglishName,
        "schoolArabicName": schoolArabicName,
      };
}
