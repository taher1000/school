import '../../domain/entities/student_data.dart';

class StudentDataModel extends StudentData {
  const StudentDataModel({
    required super.id,
    required super.studentId,
    required super.englishName,
    required super.arabicName,
    required super.academicYear,
    required super.dateOfAdmission,
    required super.dateOfBirth,
    required super.createdDate,
    required super.profilePictureId,
    required super.profilePicture,
    required super.studentAvailableReadingLevel,
    required super.studentCurrentReadingLevel,
    required super.rollNo,
    required super.isActive,
    required super.address1,
    required super.address2,
    required super.sectionId,
    required super.sectionEnglishName,
    required super.sectionArabicName,
    required super.classYearId,
    required super.classYearEnglishName,
    required super.classYearArabicName,
    required super.schoolId,
    required super.schoolEnglishName,
    required super.schoolArabicName,
    required super.gridId,
  });

  factory StudentDataModel.fromJson(Map<String, dynamic> json) =>
      StudentDataModel(
        id: json["id"],
        studentId: json["studentID"],
        englishName: json["englishName"],
        arabicName: json["arabicName"],
        academicYear: json["academicYear"] ?? "",
        dateOfAdmission: DateTime.parse(json["dateOfAdmission"]),
        dateOfBirth: DateTime.parse(json["dateOfBirth"]),
        createdDate: DateTime.parse(json["createdDate"]),
        profilePictureId: json["profilePictureID"] ?? "",
        profilePicture: json["profilePicture"] ?? "",
        studentAvailableReadingLevel: json["studentAvailableReadingLevel"],
        studentCurrentReadingLevel: json["studentCurrentReadingLevel"],
        rollNo: json["rollNo"],
        isActive: json["isActive"],
        address1: json["address1"],
        address2: json["address2"],
        sectionId: json["sectionID"],
        sectionEnglishName: json["sectionEnglishName"],
        sectionArabicName: json["sectionArabicName"],
        classYearId: json["classYearID"],
        classYearEnglishName: json["classYearEnglishName"],
        classYearArabicName: json["classYearArabicName"],
        schoolId: json["schoolID"] ?? "",
        schoolEnglishName: json["schoolEnglishName"],
        schoolArabicName: json["schoolArabicName"],
        gridId: json["gridID"],
      );
}
